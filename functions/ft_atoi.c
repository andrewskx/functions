int		ft_atoi(const char *str)
{
	int res;

	res = 0;
	while (*str == '\n' || *str == '\t' || *str == ' ')
		str++;
	if (*str < 48 || *str > 57)
		return (0);
	else
	{
		while (*str > 47 && *str < 57)
		{
			res *= 10;
			res += (int)*str - 48;
			str++;
		}
	}
	return (res);
}
