int		ft_strncmp(const char *s1, const char *s2, unsigned int len)
{
	unsigned int i;

	i = 0;
	while (s1[i] && s2[i] && (i < len) && s1[i] == s2[i])
		i++;
	return (s1[i] - s2[i]);
}
